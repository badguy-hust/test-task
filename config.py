"""
config.py —— 两个框架（qc_wild 质检 / preannotate 预标注）的全局可调参数

★ 调参只改这一个文件，全局生效。各模块从这里 import，不在别处硬编码。

放在仓库根目录；入口脚本（run_qc_wild.py / run_preannotate.py）在根目录运行，
故 qc/ qc_wild/ preannotate/ 均可 `from config import ...`。
"""

from __future__ import annotations

# ─────────────────────────────────────────────────────────────────────────────
# 探索沙箱上限（qc/explorer.py 的只读文件工具）
#   防止单次工具调用把上下文撑爆。
# ─────────────────────────────────────────────────────────────────────────────
EXPLORER_MAX_READ_CHARS = 16000           # read_file 单次最多返回字符
EXPLORER_MAX_LIST_ENTRIES = 300           # list_dir 单次最多列出条目
EXPLORER_MAX_GREP_HITS = 80               # grep 最多返回命中行
EXPLORER_MAX_FILE_BYTES = 8 * 1024 * 1024  # 超过此大小的文件只读头部


# ─────────────────────────────────────────────────────────────────────────────
# 探索充分性闸门（make_exploration_gate）—— 防止模型"随便翻两下就下结论"
#   最少读取文件数 = min(W, max(FLOOR, ceil(W * RATIO)))，再封顶 CAP。
#   W = workspace 里可读（非二进制）文件数。
# ─────────────────────────────────────────────────────────────────────────────
GATE_MIN_READS_FLOOR = 6                  # 最少读取下限（小题不强求，会被 min(W,..) 收敛）
GATE_MIN_READS_RATIO = 0.4                # 占可读文件数的比例
GATE_MIN_READS_CAP = 25                   # 封顶（再大的 workspace 也不要求读超过这么多）
GATE_MAX_NUDGES = 5                       # 闸门最多拦截几次（防与模型顶住死循环）


# ─────────────────────────────────────────────────────────────────────────────
# agentic 探索（LLM 工具循环）
# ─────────────────────────────────────────────────────────────────────────────
EXPLORE_MAX_ROUNDS = 30                   # 工具调用最多轮数
PREANNOTATE_MAX_TOKENS = 16384            # 预标注最终输出 token 上限（评分点多、需较大）
QCWILD_SEMANTIC_MAX_TOKENS = 8192         # qc_wild 语义层输出 token 上限


# ─────────────────────────────────────────────────────────────────────────────
# grade 干跑 / 自修
# ─────────────────────────────────────────────────────────────────────────────
DRY_RUN_TIMEOUT_SEC = 60                  # 空 workspace 下 grade 子进程冒烟的超时（引擎容器内为 120s）
GRADE_MAX_REPAIR = 3                      # 生成 grade 失败时，喂 stderr 让模型自修的最多轮数
GRADE_GEN_MAX_TOKENS = 16384             # 生成/自修 grade 的输出 token 上限（评分点多、judge 规范长，8192 会截断）


# ─────────────────────────────────────────────────────────────────────────────
# 生成的 grade 内 judge 行为（写进 _JUDGE_SKELETON，运行在评测容器内）
# ─────────────────────────────────────────────────────────────────────────────
JUDGE_DELIVERABLE_TEXT_BUDGET = 30000     # 喂给 judge 的单个产物文本上限（替代旧的写死 6000）
JUDGE_SAMPLES = 3                         # judge 每项自洽采样次数，取中位（防单次抽样不可复现）


# ─────────────────────────────────────────────────────────────────────────────
# 模型默认（环境变量 QC_MODEL / QC_API_URL 优先级更高，会覆盖这里）
# ─────────────────────────────────────────────────────────────────────────────
DEFAULT_MODEL = "api_aws_third_anthropic.claude-sonnet-4-5-batch"
DEFAULT_API_URL = "http://llm-api.model-eval.woa.com/v1/chat/completions"


# ─────────────────────────────────────────────────────────────────────────────
# LLM 接口调用（qc/ai_api.py）—— 单次请求超时 + 网络层重试
#   长上下文（探索轮数多、评分点多）单次推理慢，180s 易撞读超时；
#   且超时/连接类异常是瞬时故障，自动指数退避重试即可恢复，不应整题判空。
# ─────────────────────────────────────────────────────────────────────────────
AI_REQUEST_TIMEOUT = 300                  # 单次 LLM 请求读超时（秒），原写死 180
AI_MAX_RETRIES = 3                        # 超时/连接类异常的重试次数（指数退避）
AI_RETRY_BACKOFF = 2.0                    # 退避基数：第 n 次等待 BACKOFF ** n 秒
